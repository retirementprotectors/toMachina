export const firestoreConfigRoutes = Router()

// ─── Whitelist: config collections exposed by default ───
const CONFIG_COLLECTIONS = [
  'acf_config',
  'spark_config',
  'specialist_configs',
  'territories',
  'comp_grids',
  'comp_grid_history',
  'wire_definitions',
  'source_registry',
  'tool_registry',
  'atlas_formats',
  'format_library',
  'org',
] as const

// ─── TRK-507: Wiring status — auto-detected at build time ───
// Generated by: npx tsx services/api/src/scripts/detect-wiring.ts
// Runs as pre-build hook. Do NOT edit by hand.
import { readFileSync } from 'fs'
import { resolve } from 'path'

// eslint-disable-next-line @typescript-eslint/no-explicit-any
let PLATFORM_STATUS: any = {}
let WIRING_STATUS: Record<string, { status: string; backend: string; frontend: string; backend_endpoints?: number }> = {}
try {
  const wiringPath = resolve(__dirname, '..', 'generated', 'wiring-status.json')
  PLATFORM_STATUS = JSON.parse(readFileSync(wiringPath, 'utf-8'))
  // Support both old format (root = collections) and new format (root.collections)
  WIRING_STATUS = PLATFORM_STATUS.collections || PLATFORM_STATUS
} catch {
  console.warn('[firestore-config] wiring-status.json not found — run detect-wiring.ts')
}

// Queue collections for health dashboard
const QUEUE_COLLECTIONS = ['intake_queue', 'approval_queue'] as const

// ─── Helpers ───
function detectFieldType(value: unknown): string {
  if (value === null || value === undefined) return 'null'
  if (typeof value === 'boolean') return 'boolean'
  if (typeof value === 'number') return 'number'
  if (typeof value === 'string') return 'string'
  if (Array.isArray(value)) return 'array'
  if (typeof value === 'object') return 'object'
  return 'unknown'
}

function buildTypeSummary(data: Record<string, unknown>): Record<string, number> {
  const counts: Record<string, number> = {}
  for (const val of Object.values(data)) {
    const t = detectFieldType(val)
    counts[t] = (counts[t] || 0) + 1
  }
  return counts
}

function isValidSnakeCase(name: string): boolean {
  return /^[a-z][a-z0-9_]*$/.test(name)
}

// ─── TRK-507: GET /collections — with wiring status ───
firestoreConfigRoutes.get('/collections', async (_req: Request, res: Response) => {
  try {
    const db = getFirestore()
    const snapshots = await Promise.all(
      CONFIG_COLLECTIONS.map(name => db.collection(name).get())
    )

    const results = CONFIG_COLLECTIONS.map((name, i) => {
      const snap = snapshots[i]
      const docs = snap.docs.map(doc => {
        const data = doc.data() as Record<string, unknown>
        return {
          id: doc.id,
          fieldCount: Object.keys(data).length,
          typeSummary: buildTypeSummary(data),
          updatedAt: (data.updated_at as string) || (data.updatedAt as string) || null,
          data,
        }
      })
      return {
        name,
        count: snap.size,
        docs,
        wiring: WIRING_STATUS[name] || { status: 'placeholder' as const, backend: '', frontend: '' },
      }
    })

    res.json(successResponse<FirestoreConfigCollectionsData>(results as unknown as FirestoreConfigCollectionsData))
  } catch (err) {
    console.error('GET /api/firestore-config/collections error:', err)
    res.status(500).json(errorResponse('Failed to load config collections'))
  }
})

